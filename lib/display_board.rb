# Define a method display_board that prints a 3x3 Tic Tac Toe Board
def display_board
  puts "A Tic Tac Toe Board"
  row = '"   "+"   "+"   "'
  seperated = "-----------"
  space = "   |   |   "
  puts row
  puts space
  puts seperated
  puts row
  puts space
  puts seperated
  puts row
  puts space
end
